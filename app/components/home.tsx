function Screen() {
  const config = useAppConfig();
  const location = useLocation();
  const isArtifact = location.pathname.includes(Path.Artifacts);
  const isHome = location.pathname === Path.Home;
  const isAuth = location.pathname === Path.Auth;
  const isSd = location.pathname === Path.Sd;
  const isSdNew = location.pathname === Path.SdNew;

  const isMobileScreen = useMobileScreen();
  const shouldTightBorder =
    getClientConfig()?.isApp || (config.tightBorder && !isMobileScreen);

  // {{CHENGQI:
  // Action: Removed Google Fonts loading call for Apple HIG compliance
  // Timestamp: 2025-06-22
  // Reason: Apple HIG recommends using system fonts exclusively
  // Principle_Applied: Apple Human Interface Guidelines - Typography
  // Optimization: No external font loading needed, better performance
  // Architectural_Note (AR): System fonts provide optimal accessibility
  // Documentation_Note (DW): Google Fonts loading removed for Apple compliance
  // }}

  // Apple HIG Compliance: No external font loading needed
  // System fonts defined in globals.scss provide optimal experience

  if (isArtifact) {
    return (
      <Routes>
        <Route path="/artifacts/:id" element={<Artifacts />} />
      </Routes>
    );
  }
  const renderContent = () => {
    if (isAuth) return <AuthPage />;
    if (isSd) return <Sd />;
    if (isSdNew) return <Sd />;
    return (
      <>
        <SideBar
          className={clsx({
            [styles["sidebar-show"]]: isHome,
          })}
        />
        <WindowContent>
          <Routes>
            <Route path={Path.Home} element={<Chat />} />
            <Route path={Path.NewChat} element={<NewChat />} />
            <Route path={Path.Masks} element={<MaskPage />} />
            <Route path={Path.Plugins} element={<PluginPage />} />
            <Route path={Path.SearchChat} element={<SearchChat />} />
            <Route path={Path.Chat} element={<Chat />} />
            <Route path={Path.Settings} element={<Settings />} />
            {/* MCP市场路由已移除 - 生产环境清理 */}
          </Routes>
        </WindowContent>
      </>
    );
  };

  return (
    <div
      className={clsx(styles.container, {
        [styles["tight-container"]]: shouldTightBorder,
        [styles["rtl-screen"]]: getLang() === "ar",
      })}
    >
      {renderContent()}
    </div>
  );
}

export function useLoadData() {
  const config = useAppConfig();

  const api: ClientApi = getClientApi(config.modelConfig.providerName);

  useEffect(() => {
    (async () => {
      // {{CHENGQI:
      // Action: Added - 初始化自定义模型数据
      // Timestamp: 2025-06-18 17:25:00 +08:00
      // Reason: 在应用启动时初始化自定义模型数据
      // Principle_Applied: SOLID - 单一职责，初始化逻辑
      // Optimization: 应用启动时的数据迁移
      // Architectural_Note (AR): 应用初始化的最佳实践
      // Documentation_Note (DW): 自定义模型数据的初始化调用
      // }}
      config.initialize();

      const models = await api.llm.models();
      config.mergeModels(models);
    })();
    // eslint-disable-next-line react-hooks/exhaustive-deps
  }, []);
}

